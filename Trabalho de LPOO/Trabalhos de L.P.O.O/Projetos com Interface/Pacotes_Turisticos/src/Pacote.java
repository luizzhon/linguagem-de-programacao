
public class Pacote {
    
    private String destino;
    private int N_Dias;
    private int N_Pessoas;
    private String Categoria_Hotel;
    private double Valor_Pacote;

    /**
     * @return the destino
     */
    public String getDestino() {
        return destino;
    }

    /**
     * @param destino the destino to set
     */
    public void setDestino(String destino) {
        this.destino = destino;
    }

    /**
     * @return the N_Dias
     */
    public int getN_Dias() {
        return N_Dias;
    }

    /**
     * @param N_Dias the N_Dias to set
     */
    public void setN_Dias(int N_Dias) {
        this.N_Dias = N_Dias;
    }

    /**
     * @return the N_Pessoas
     */
    public int getN_Pessoas() {
        return N_Pessoas;
    }

    /**
     * @param N_Pessoas the N_Pessoas to set
     */
    public void setN_Pessoas(int N_Pessoas) {
        this.N_Pessoas = N_Pessoas;
    }

    /**
     * @return the Categoria_Hotel
     */
    public String getCategoria_Hotel() {
        return Categoria_Hotel;
    }

    /**
     * @param Categoria_Hotel the Categoria_Hotel to set
     */
    public void setCategoria_Hotel(String Categoria_Hotel) {
        this.Categoria_Hotel = Categoria_Hotel;
    }

    /**
     * @return the Valor_Pacote
     */
    public double getValor_Pacote() {
        return Valor_Pacote;
    }

    /**
     * @param Valor_Pacote the Valor_Pacote to set
     */
    public void setValor_Pacote(double Valor_Pacote) {
        this.Valor_Pacote = Valor_Pacote;
    }
    
}
